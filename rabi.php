Rabindranath Tagore FRAS (/rəˈbɪndrənɑːt tæˈɡɔːr/ (About this soundlisten); born Robindronath Thakur,[1] 7 May 1861 – 7 August 1941),[a] and also known by his sobriquets Gurudev,[b] Kabiguru, and Biswakabi, was a Bengali polymath, poet, musician, and artist from the Indian subcontinent.[4][5] He reshaped Bengali literature and music, as well as Indian art with Contextual Modernism in the late 19th and early 20th centuries. Author of the "profoundly sensitive, fresh and beautiful verse" of Gitanjali,[6] he became in 1913 the first non-European to win the Nobel Prize in Literature.[7] Tagore's poetic songs were viewed as spiritual and mercurial; however, his "elegant prose and magical poetry" remain largely unknown outside Bengal.[8] He is sometimes referred to as "the Bard of Bengal".[9]
<p>A Brhamo from Calcutta with ancestral gentry roots in Jessore, Tagore wrote poetry as an eight-year-old.[10] At the age of sixteen, he released his first substantial poems under the pseudonym Bhānusiṃha ("Sun Lion"), which were seized upon by literary authorities as long-lost classics.[11][12] By 1877 he graduated to his first short stories and dramas, published under his real name. As a humanist, universalist, internationalist, and ardent anti-nationalist,[13] he denounced the British Raj and advocated independence from Britain. As an exponent of the Bengal Renaissance, he advanced a vast canon that comprised paintings, sketches and doodles, hundreds of texts, and some two thousand songs; his legacy also endures in the institution he founded, Visva-Bharati University.</p>

<p>Tagore modernised Bengali art by spurning rigid classical forms and resisting linguistic strictures. His novels, stories, songs, dance-dramas, and essays spoke to topics political and personal. Gitanjali (Song Offerings), Gora (Fair-Faced) and Ghare-Baire (The Home and the World) are his best-known works, and his verse, short stories, and novels were acclaimed—or panned—for their lyricism, colloquialism, naturalism, and unnatural contemplation. His compositions were chosen by two nations as national anthems: India's Jana Gana Mana and Bangladesh's Amar Shonar Bangla. The Sri Lankan national anthem was inspired by his work.</p>



